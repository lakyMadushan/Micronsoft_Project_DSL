    public void tableload() {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        try {
            ResultSet rs = DB.search("select * from guest_arr_dep");
            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("t_id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("pass_num"));
                v.add(rs.getString("a_date"));
                v.add(rs.getString("a_flight_num"));
                v.add(rs.getString("d_date"));
                v.add(rs.getString("d_flight_num"));
                v.add(rs.getString("remark"));

                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

