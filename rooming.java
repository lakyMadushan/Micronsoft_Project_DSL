    private void comboLoad() {
        try {
            jComboBox1.removeAllItems();
            ResultSet rs = DB.search("select * from r_type order by type");
            while (rs.next()) {
                jComboBox1.addItem(rs.getString("type"));

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void idLoad() {
        try {
            ResultSet rs = DB.search("select id from rooming order by id DESC");
            if (rs.next()) {
                jTextField1.setText(String.valueOf(Integer.parseInt(rs.getString("id")) + 1));
            } else {
                jTextField1.setText("1");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

