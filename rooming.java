    private void jTable1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTable1KeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {

            //select table data 
            try {
                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

                if (jTable1.getSelectedRow() != -1) {

                    String id = dtm.getValueAt(jTable1.getSelectedRow(), 0).toString();
                    String tid = dtm.getValueAt(jTable1.getSelectedRow(), 1).toString();
                    String pass = dtm.getValueAt(jTable1.getSelectedRow(), 2).toString();
                    String name = dtm.getValueAt(jTable1.getSelectedRow(), 3).toString();
                    String rtype = dtm.getValueAt(jTable1.getSelectedRow(), 4).toString();
                    String qty = dtm.getValueAt(jTable1.getSelectedRow(), 5).toString();

                    jTextField1.setText(id);
                    jTextField2.setText(tid);
                    jTextField3.setText(pass);
                    jTextField4.setText(name);
                    jComboBox1.setSelectedItem(rtype);
                    jTextField6.setText(qty);
                    jButton2.setEnabled(false);

                } else {
                    JOptionPane.showMessageDialog(this, "Not Found !", "Info", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jTable1KeyPressed

