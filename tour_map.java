    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // save
        if (jTextField2.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Please Enter data before Save !", "Error", JOptionPane.INFORMATION_MESSAGE, erricon);
        } else {
            try {
                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                for (int i = 0; i < dtm.getRowCount(); i++) {

                    String t_id = dtm.getValueAt(jTable1.getSelectedRow(), 1).toString();
                    String pass = dtm.getValueAt(jTable1.getSelectedRow(), 2).toString();
                    String name = dtm.getValueAt(jTable1.getSelectedRow(), 3).toString();
                    String guid = dtm.getValueAt(jTable1.getSelectedRow(), 4).toString();
                    String date = dtm.getValueAt(jTable1.getSelectedRow(), 5).toString();
                    String location = dtm.getValueAt(jTable1.getSelectedRow(), 6).toString();

                    DB.iud("insert into tour_map (t_id,passport,name,guid,date,location) "
                            + "values ( '" + t_id + "', '" + pass + "','" + name + "','" + guid + "','" + date + "','" + location + "' )");

                    JOptionPane.showMessageDialog(this, "Insert successfully.", "DONE", JOptionPane.INFORMATION_MESSAGE, icon);
                    idLoad();
                }
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Save Fail !", "Error", JOptionPane.INFORMATION_MESSAGE, erricon);

            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

