                        String name = dtm.getValueAt(jTable1.getSelectedRow(), 3).toString();
                        String guid = dtm.getValueAt(jTable1.getSelectedRow(), 4).toString();
                        String date = dtm.getValueAt(jTable1.getSelectedRow(), 5).toString();
                        String location = dtm.getValueAt(jTable1.getSelectedRow(), 6).toString();

                        DB.iud("insert into tour_map (t_id,passport,name,guid,date,location) "
                                + "values ( '" + t_id + "', '" + pass + "','" + name + "','" + guid + "','" + date + "','" + location + "' )");

                        JOptionPane.showMessageDialog(this, "Insert successfully.", "DONE", JOptionPane.INFORMATION_MESSAGE, icon);
