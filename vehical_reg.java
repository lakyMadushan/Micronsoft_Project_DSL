    private void tableLoad() {
        try {
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            ResultSet rs = DB.search("select * from vehicals");
            while (rs.next()) {
                Vector<Object> v = new Vector<>();
                v.add(rs.getString("id"));
                v.add(rs.getString("vehical"));
                v.add(rs.getString("no"));
                dtm.addRow(v);
            }
            idLoad();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Table loading Unsuccesfully !", "ERROR", JOptionPane.INFORMATION_MESSAGE, erricon);
        }
    }

    private void idLoad() {
        try {
            ResultSet rs = DB.search("select id from vehicals order by id DESC");
            if (rs.next()) {
                jLabel4.setText(String.valueOf(Integer.parseInt(rs.getString("id")) + 1));
            } else {
                jLabel4.setText("1");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
