    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try {
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            if (jTable1.getSelectedRow() != -1) {
                String id = dtm.getValueAt(jTable1.getSelectedRow(), 0).toString();
                String vehical = dtm.getValueAt(jTable1.getSelectedRow(), 1).toString();
                String no = dtm.getValueAt(jTable1.getSelectedRow(), 2).toString();

                jLabel4.setText(id);
                jTextField1.setText(vehical);
                jTextField2.setText(no);
                jButton1.setEnabled(false);
            } else {
                System.out.println("No data found for ID: " + jLabel4.getText());
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jTable1MouseClicked

